    private ArrayList<Budget> getBudgetListByMonth(Month selectedMonth){
        MonthlyBudget budgets = frame.controller.getMonthlyBudget(selectedMonth);
        
        ArrayList<Budget> budgetList = new ArrayList();
        
        if (budgets != null)
            budgetList = budgets.getBudgetList();
        
        return budgetList;
    }
    
    
    
