public void updateTableModel(ArrayList<Budget> budgetList){
        //Create new table model
        DefaultTableModel tableModel = new DefaultTableModel();

        int columnCount = 3;
        
        
        tableModel.addColumn("Name");
        tableModel.addColumn("Amount");
        tableModel.addColumn("Final Balance");
        
        tableModel.setRowCount(budgetList.size());
        int row = 0;
        //Scroll through transactions
        
        for (Budget b : budgetList){
            tableModel.setValueAt(b.getCategory(), row, 0);
            //debug
            System.out.println(b.getCategory());
            
            tableModel.setValueAt(b.getAmount(), row, 1);
            tableModel.setValueAt(b.getBalance(), row, 2);
            row++;
        }

        //Now add that table model to your table and you are done :D
        table.setModel(tableModel);
    }
